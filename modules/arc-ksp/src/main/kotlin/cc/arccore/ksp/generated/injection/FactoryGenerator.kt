package cc.arccore.ksp.generated.injection

import cc.arccore.ksp.model.InjectableEntry
import com.google.devtools.ksp.processing.CodeGenerator
import com.google.devtools.ksp.processing.Dependencies
import com.google.devtools.ksp.processing.KSPLogger
import com.google.devtools.ksp.symbol.KSFile
import com.squareup.kotlinpoet.*
import com.squareup.kotlinpoet.ParameterizedTypeName.Companion.parameterizedBy
import com.squareup.kotlinpoet.ksp.writeTo

/**
 * Generates one `Generated<ClassName>Factory.kt` file per injectable class.
 *
 * The generated factory implements [cc.arccore.api.di.generated.GeneratedInjector]
 * and calls the target constructor directly. Example output:
 *
 * ```kotlin
 * // generated — do not edit
 * package com.example.module.generated.di
 *
 * class GeneratedBalanceCommandFactory : GeneratedInjector<BalanceCommand> {
 *     override val targetClass = BalanceCommand::class
 *     override val scope: Scope = Scope.Transient
 *     override val metadataVersion: Int = 1
 *
 *     override fun create(context: InjectionContext): BalanceCommand {
 *         val economyService = context.resolve(EconomyService::class)
 *         return BalanceCommand(economyService)
 *     }
 * }
 * ```
 *
 * WHY the generated factory is faster than reflection:
 * - `context.resolve(EconomyService::class)` is a map lookup (O(1)) followed
 *   by a direct constructor call. The JIT can inline both.
 * - `constructor.newInstance()` acquires a lock, checks access, boxes each
 *   argument, and throws InvocationTargetException on any error — none of
 *   which can be eliminated by the JIT because the call site is polymorphic.
 * - No `KParameter`, `KFunction`, or `Annotation` objects are allocated per call.
 * - With enough call-site monomorphism the JIT can devirtualize `create()`,
 *   making the factory call as fast as a regular constructor call.
 */
object FactoryGenerator {

    private val GENERATED_INJECTOR = ClassName("cc.arccore.api.di.generated", "GeneratedInjector")
    private val INJECTION_CONTEXT = ClassName("cc.arccore.api.di.generated", "InjectionContext")
    private val SCOPE = ClassName("cc.arccore.api.di", "Scope")
    private val KCLASS = ClassName("kotlin.reflect", "KClass")

    fun generate(
        codeGenerator: CodeGenerator,
        logger: KSPLogger,
        entry: InjectableEntry,
        originatingFiles: List<KSFile>
    ) {
        val targetClass = entry.toClassName()
        val factoryPkg = entry.generatedPackage()
        val factorySimple = "Generated${entry.simpleName}Factory"

        val scopeExpr = when (entry.scope) {
            InjectableEntry.InjectableScope.SINGLETON -> CodeBlock.of("%T.Singleton", SCOPE)
            InjectableEntry.InjectableScope.MODULE -> CodeBlock.of("%T.Module", SCOPE)
            InjectableEntry.InjectableScope.TRANSIENT -> CodeBlock.of("%T.Transient", SCOPE)
        }

        val createBody = CodeBlock.builder()
        entry.constructorParams.forEach { param ->
            val paramClass = fqcnToClassName(param.typeFqcn)
            createBody.addStatement("val %N = context.resolve(%T::class)", param.name, paramClass)
        }
        val argList = entry.constructorParams.joinToString(", ") { it.name }
        if (argList.isEmpty()) {
            createBody.addStatement("return %T()", targetClass)
        } else {
            createBody.addStatement("return %T(%L)", targetClass, argList)
        }

        val createFn = FunSpec.builder("create")
            .addModifiers(KModifier.OVERRIDE)
            .addParameter("context", INJECTION_CONTEXT)
            .returns(targetClass)
            .addCode(createBody.build())
            .build()

        val factoryClass = TypeSpec.classBuilder(factorySimple)
            .addSuperinterface(GENERATED_INJECTOR.parameterizedBy(targetClass))
            .addProperty(
                PropertySpec.builder("targetClass", KCLASS.parameterizedBy(targetClass))
                    .addModifiers(KModifier.OVERRIDE)
                    .initializer("%T::class", targetClass)
                    .build()
            )
            .addProperty(
                PropertySpec.builder("scope", SCOPE)
                    .addModifiers(KModifier.OVERRIDE)
                    .initializer(scopeExpr)
                    .build()
            )
            .addProperty(
                PropertySpec.builder("metadataVersion", Int::class)
                    .addModifiers(KModifier.OVERRIDE)
                    .initializer("1")
                    .build()
            )
            .addFunction(createFn)
            .build()

        FileSpec.builder(factoryPkg, factorySimple)
            .addFileComment("Generated by ARCCore KSP — do not edit manually.")
            .addType(factoryClass)
            .build()
            .writeTo(codeGenerator, Dependencies(aggregating = false, *originatingFiles.toTypedArray()))

        logger.info("[ARCCore DI] Generated factory: $factoryPkg.$factorySimple → ${entry.className}")
    }

    fun factoryFqcn(entry: InjectableEntry): String =
        "${entry.generatedPackage()}.Generated${entry.simpleName}Factory"

    private fun InjectableEntry.toClassName(): ClassName =
        ClassName(packageName, simpleName)

    private fun InjectableEntry.generatedPackage(): String =
        if (packageName.isEmpty()) "generated.di" else "$packageName.generated.di"

    private fun fqcnToClassName(fqcn: String): ClassName {
        val dot = fqcn.lastIndexOf('.')
        return if (dot < 0) ClassName("", fqcn)
        else ClassName(fqcn.substring(0, dot), fqcn.substring(dot + 1))
    }
}
